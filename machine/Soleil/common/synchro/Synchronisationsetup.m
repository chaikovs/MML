set(b2(5), 'Callback','disp([''   Kickers ANS;'']); set_bump4;', 'String',' Set kickers bump optim 4');
set(b2(6), 'Callback','disp([''   Kickers ANS;'']); set_bump5;', 'String',' Set kickers bump optim 5');


b = uicontrol('Parent',a, ...
    'Callback','close all', ...
    'String','Close All Figures', ...
    'Position',[3 height-kmax*30 204 27], ...
    'Userdata',[], ...
    'Interruptible', 'off', ...   
    'Tag','button2');

