    b2(k) = uicontrol('Parent',a, ...
        'Position',[3 + 210 height-(k-1)*30 204 27], ...
        'Interruptible', 'off', ...
        'Tag','button22');
end

set(b1(1), 'Callback','disp([''   Timing;'']); synchro_injecteur7;', 'String','Synchronisation');
set(b1(2), 'Callback','disp([''   Fichiers de Consignes;'']); save_boo;', 'String','Fichiers de Consignes');
